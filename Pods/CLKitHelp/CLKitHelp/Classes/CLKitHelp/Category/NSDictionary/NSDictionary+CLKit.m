#import "NSDictionary+CLKit.h"

@implementation NSDictionary (CLKit)

- (NSString *)clKitDictionaryToJson
{
    NSError *error;
    NSData *jsonData = [NSJSONSerialization dataWithJSONObject:self options:NSJSONWritingPrettyPrinted error:&error];
    NSString *jsonString;
    if (!jsonData) {
        NSLog(@"%@", error);
    } else {
        jsonString = [[NSString alloc]initWithData:jsonData encoding:NSUTF8StringEncoding];
    }
    NSMutableString *mutStr = [NSMutableString stringWithString:jsonString];
    NSRange range = { 0, jsonString.length };
    //去掉字符串中的空格
    [mutStr replaceOccurrencesOfString:@" " withString:@"" options:NSLiteralSearch range:range];
    NSRange range2 = { 0, mutStr.length };
    //去掉字符串中的换行符
    [mutStr replaceOccurrencesOfString:@"\n" withString:@"" options:NSLiteralSearch range:range2];
    return mutStr;
}

+ (NSDictionary *)clKitInitWithJson:(NSString *)json
{
    if (json == nil) {
        return nil;
    }

    NSData *jsonData = [json dataUsingEncoding:NSUTF8StringEncoding];
    NSError *err;
    NSDictionary *dic = [NSJSONSerialization JSONObjectWithData:jsonData
                                                        options:NSJSONReadingMutableContainers
                                                          error:&err];
    if (err) {
        NSLog(@"json解析失败：%@", err);
        return nil;
    }
    return dic;
}

@end

@implementation NSMutableDictionary (CLKit)

/// 安全模式设定值
/// @param object 值
/// @param aKey key
- (BOOL)clKitSetObject:(id)object
                forKey:(id<NSCopying>)aKey
{
    if (nil == object) {
        return NO;
    }

    [self setObject:object
             forKey:aKey];
    return YES;
}

@end
